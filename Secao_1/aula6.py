# Conversão de tipos, coerção, type convertion, typecasting, corecion
# é o ato de converter um tipo em outro.
# Tipos imutáveis e primitivos:
# str, int, float, bool

print(1 + 1) # 2
print('a' + 'b') # 'ab'

# print('1' + 1) #TypeError

print(int('1') + 1) # 2
print(float('1') + 1) # 2.0
print(bool(' '))
print(bool(''))
print(str(29) + 'b')