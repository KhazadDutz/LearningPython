"""
Python = Linguagem de programacao
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
String sao textos que estao dentro de aspas
"""

print(1234)

# Aspas simples
print('Khazad Dutz')

# Aspas duplas
print("Khazad Dutz")

# Escape
print('Khazad \"Dutz')
print('Khazad \"Dutz\"')

# r
print(r"Khazad \"Dutz")

# Aspas simples dentro de Aspas duplas
print("Khazad 'Dutz'")

# Aspas duplas dentro de Aspas simples
print('Khazad "Dutz"')
